import { TimeKeeper } from './TimeKeeper';
export class Timeable{
  /**************************************************************************************************************
  Fields
  ***************************************************************************************************************/
  lastUpdateTime :       number;
  getDeltaTime   : () => number;

  /**************************************************************************************************************
  Constructors
  ***************************************************************************************************************/
  constructor(lastUpdateTime?:number){
    this.lastUpdateTime = lastUpdateTime;
  }

  /**************************************************************************************************************
  SetUpdate
  ***************************************************************************************************************/
  setUpdateFunction(tk:TimeKeeper){
    this.getDeltaTime = () :number =>{return tk.getTimeSince(this.lastUpdateTime)};
  }

}

